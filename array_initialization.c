#include<stdio.h>

int main(){
    int a[] = {0001, 9999, 6244};
    printf("The value of a[0] is %d \n", a[0]);
    printf("The value of a[1] is %d \n", a[1]);
    printf("The value of a[2] is %d \n", a[2]);
    printf("\n");

    char i[] = {'a','b','c','d'};
    printf("The value of i[0] is %c \n", i[0]);
    printf("The value of i[1] is %c \n", i[1]);
    printf("The value of i[2] is %c \n", i[2]);
    printf("The value of i[3] is %c \n", i[3]);

    return 0;
}